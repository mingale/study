package spring.mvc.bookstore.vo;

public class StringInt {
	private String str;
	private int count;
	private String md;

	public String getStr() {
		return str;
	}

	public void setStr(String str) {
		this.str = str;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public String getMd() {
		return md;
	}
	public void setMd(String md) {
		this.md = md;
	}
}
